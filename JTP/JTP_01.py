# Addition of one numbers

def number(n):
    return n+n
n = int(input("Enter an number: "))
result = number(n)
print(result)

# Addition of two numbers

def two_addition(a, b):
    return a+b
a = int(input("Enter the first number: "))
b = int(input("Enter the second: "))

result = two_addition(a, b)
print(result)

